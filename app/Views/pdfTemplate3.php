        <li>10 Kementerian/Lembaga: Kementerian PPN/Bappenas, Kementerian ATR/BPN, Kementerian Dalam Negeri, Kementerian Desa dan Pembangunan Daerah Tertinggal, Kementerian Transmigrasi, Kementerian Pertanian, Kementerian Kelautan dan Perikanan, Kementerian Perhubungan, Kementerian Pariwisata, dan Badan Nasional Pengelola Perbatasan;</li>
