    <p>Pada tanggal 18 s.d. 22 November 2024 telah dilaksanakan pembahasan keterpaduan program Bidang Pekerjaan Umum tahun 2024 dalam forum Desk Rakorbangwil, yang diikuti oleh perwakilan dari:</p>
